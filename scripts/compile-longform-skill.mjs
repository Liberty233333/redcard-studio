import { mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';

const root = resolve(import.meta.dirname, '..');
const sourcePath = resolve(root, 'docs/knowledge/prompts/longform-skill-v3.5.md');
const fallbackSourcePath = resolve(root, 'docs/references/longform-skill-v3.5.md');
const outputPath = resolve(root, 'src/llm/_longformSkill.generated.ts');

let skill = '';
try {
  skill = readFileSync(sourcePath, 'utf8').trim();
} catch {
  skill = readFileSync(fallbackSourcePath, 'utf8').trim();
}

const generated = `// Auto-generated by scripts/compile-longform-skill.mjs. Do not edit by hand.
export const longformSkillV35 = ${JSON.stringify(skill)} as const;
`;

mkdirSync(dirname(outputPath), { recursive: true });
writeFileSync(outputPath, generated);
