import { mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';

const root = resolve(import.meta.dirname, '..');
const sourcePath = resolve(root, 'docs/knowledge/cases/exemplar-articles.md');
const fallbackSourcePath = resolve(root, 'docs/references/exemplar-articles.md');
const outputPath = resolve(root, 'src/llm/_exemplars.generated.ts');

function hash(input) {
  let h = 0x811c9dc5;
  for (let i = 0; i < input.length; i += 1) {
    h ^= input.charCodeAt(i);
    h = Math.imul(h, 0x01000193);
  }
  return `ex_${(h >>> 0).toString(16).padStart(8, '0')}`;
}

function extractSection(block, title) {
  const match = block.match(new RegExp(`### ${title}\\n+([\\s\\S]*?)(?=\\n### |\\n---\\n|$)`));
  return match?.[1]?.trim() || '';
}

function parseTags(text) {
  const tags = {};
  for (const line of text.split('\n')) {
    const match = line.match(/^([^:：]+)[:：]\s*(.*?)\s*$/);
    if (match) tags[match[1].trim()] = match[2].trim();
  }
  return tags;
}

let markdown = '';
try {
  markdown = readFileSync(sourcePath, 'utf8');
} catch {
  try {
    markdown = readFileSync(fallbackSourcePath, 'utf8');
  } catch {
    markdown = '';
  }
}

const exemplars = markdown
  .split(/## Exemplar\s+\d+/)
  .slice(1)
  .map((block) => {
    const sourceMaterial = extractSection(block, 'Source material');
    const finalArticle = extractSection(block, 'Final article');
    const tags = parseTags(extractSection(block, 'Tags'));
    if (!sourceMaterial || !finalArticle || /\[Paste|\[\.\.\.|\[e\.g\./.test(`${sourceMaterial}\n${finalArticle}`)) {
      return null;
    }
    return {
      id: hash(`${sourceMaterial}\n${finalArticle}`),
      sourceMaterial,
      finalArticle,
      tags: {
        contentType: tags.content_type || tags.contentType || '',
        performedWellOn: tags.performed_well_on || tags.performedWellOn || '',
      },
    };
  })
  .filter(Boolean);

const generated = `// Auto-generated by scripts/compile-exemplars.mjs. Do not edit by hand.
export interface ExemplarEntry {
  id: string;
  sourceMaterial: string;
  finalArticle: string;
  tags: { contentType: string; performedWellOn: string };
}

export const exemplarRegistry: readonly ExemplarEntry[] = ${JSON.stringify(exemplars, null, 2)};
`;

mkdirSync(dirname(outputPath), { recursive: true });
writeFileSync(outputPath, generated);
