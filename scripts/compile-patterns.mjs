import { mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';

const root = resolve(import.meta.dirname, '..');
const sourcePath = resolve(root, 'docs/knowledge/rules/banned-patterns.md');
const fallbackSourcePath = resolve(root, 'docs/references/banned-patterns.md');
const outputPath = resolve(root, 'src/validators/_patterns.generated.ts');

let markdown = '';
try {
  markdown = readFileSync(sourcePath, 'utf8');
} catch {
  markdown = readFileSync(fallbackSourcePath, 'utf8');
}

function section(title) {
  const escaped = title.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
  const match = markdown.match(new RegExp(`## ${escaped}[\\s\\S]*?(?=\\n## |$)`));
  return match?.[0] || '';
}

function syntaxRows(sectionText) {
  return sectionText
    .split('\n')
    .map((line) => line.match(/^\|\s*`([^`]+)`\s*\|\s*([^|]*?)\s*\|/))
    .filter(Boolean)
    .map((match) => ({
      pattern: match[1],
      maxOccurrences: parseMaxOccurrences(match[2]),
    }));
}

function bullets(sectionText) {
  return sectionText
    .split('\n')
    .map((line) => line.match(/^\s*-\s+`?([^`\n]+?)`?\s*$/)?.[1]?.trim())
    .filter(Boolean);
}

function fixTemplates() {
  const rows = [...markdown.matchAll(/\|\s*([^|`]+?)(?:\s+\(`([^`]+)`\))?\s*\|\s*"([^"]+)"\s*\|/g)];
  const syntax = {};
  let phrase = '';
  let emptyPraise = '';
  let opening = '';
  for (const row of rows) {
    const category = row[1].trim();
    const pattern = row[2]?.trim();
    const template = row[3].trim();
    if (category === 'syntax' && pattern?.includes('不是')) {
      syntax['不是.{1,30}而是'] = template;
      syntax['不是[^，。？！\\n]{1,15}，[^，。？！\\n]{1,15}是'] = template;
      syntax['不是[^，。？！\\n]{1,15}，是[^，。？！\\n]{1,15}'] = template;
    }
    if (category === 'syntax' && pattern === 'em-dash') {
      syntax['——'] = template;
      syntax['(?<!-)--(?!-)'] = template;
      syntax['---+'] = template;
    }
    if (category === 'phrase') phrase = template;
    if (category === 'empty-praise') emptyPraise = template;
    if (category === 'opening') opening = template;
  }
  const emDashRow = markdown.match(/\|\s*syntax \(em-dash\)\s*\|\s*"([^"]+)"\s*\|/);
  if (emDashRow) {
    syntax['——'] = emDashRow[1].trim();
    syntax['(?<!-)--(?!-)'] = emDashRow[1].trim();
    syntax['---+'] = emDashRow[1].trim();
  }
  return { syntax, phrase, emptyPraise, opening };
}

function parseMaxOccurrences(value) {
  const trimmed = value.trim();
  if (!trimmed) return null;
  const parsed = Number(trimmed);
  return Number.isFinite(parsed) ? parsed : null;
}

const syntax = syntaxRows(section('Category: syntax'));
const phrase = bullets(section('Category: phrase'));
const emptyPraise = bullets(section('Category: empty-praise'));
const opening = bullets(section('Category: opening'));
const templates = fixTemplates();

const generated = `// Auto-generated by scripts/compile-patterns.mjs. Do not edit by hand.
export const patternRegistry = ${JSON.stringify(
  {
    syntax: syntax.map((item) => ({
      pattern: item.pattern,
      source: patternSource(item.pattern),
      flags: item.pattern === '不是.{1,30}而是' ? 'gs' : 'g',
      maxOccurrences: item.maxOccurrences,
    })),
    phrase,
    emptyPraise,
    opening: opening.map((pattern) => ({ pattern, source: pattern })),
    fixTemplates: templates,
  },
  null,
  2
)} as const;
`;

mkdirSync(dirname(outputPath), { recursive: true });
writeFileSync(outputPath, generated);

function patternSource(pattern) {
  if (pattern === '不是.{1,30}而是') return '不是.{1,30}?而是';
  return pattern;
}
