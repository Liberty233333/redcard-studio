import { mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';

const root = resolve(import.meta.dirname, '..');
const outputPath = resolve(root, 'src/knowledge/_knowledge.generated.ts');

function readKnowledge(relativePath, fallbackRelativePath = '') {
  const primary = resolve(root, relativePath);
  try {
    return readFileSync(primary, 'utf8').trim();
  } catch {
    if (!fallbackRelativePath) return '';
    try {
      return readFileSync(resolve(root, fallbackRelativePath), 'utf8').trim();
    } catch {
      return '';
    }
  }
}

const generated = `// Auto-generated by scripts/compile-knowledge-base.mjs. Do not edit by hand.
export const knowledgeRegistry = ${JSON.stringify(
  {
    rules: {
      bannedPatterns: readKnowledge('docs/knowledge/rules/banned-patterns.md', 'docs/references/banned-patterns.md'),
    },
    cases: {
      exemplarArticles: readKnowledge('docs/knowledge/cases/exemplar-articles.md', 'docs/references/exemplar-articles.md'),
    },
    prompts: {
      'longform-skill-v3.5': readKnowledge('docs/knowledge/prompts/longform-skill-v3.5.md', 'docs/references/longform-skill-v3.5.md'),
      'spec-extractor': readKnowledge('docs/knowledge/prompts/spec-extractor.md'),
    },
    styleProfile: {
      accountVoice: readKnowledge('docs/knowledge/style-profile/account-voice.md'),
      coverVoiceProfile: readKnowledge('docs/knowledge/style-profile/cover-voice-profile.md'),
      coverSamplesIndex: readKnowledge('docs/knowledge/style-profile/cover-samples-index.md'),
    },
  },
  null,
  2
)} as const;
`;

mkdirSync(dirname(outputPath), { recursive: true });
writeFileSync(outputPath, generated);
